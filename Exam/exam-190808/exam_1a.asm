		MOV AX,X 
		MUL X 
		MOV WORD PTR X2,AX
		MOV WORD PTR [X2+2],DX
		MOV	WORD PTR XY2,AX
		MOV WORD PTR [XY2+2],DX
		MOV AX,Y
