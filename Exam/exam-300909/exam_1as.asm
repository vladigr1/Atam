		.MODEL SMALL
		.STACK 100h
		.DATA
		.CODE
		.386
	_find_value PROC NEAR
		PUBLIC _find_value
		PUSH BP
		MOV BP,SP
		MOV BX,[BP+4]
		MOV CX,[BP+6]
		INC CX
		JMP CHECK_LOOP
START_LOOP:
		MOV EAX,[BX]
		CMP EAX,[BP+8]
		JNE NOT_EQ
		MOV AX,BX
		JMP ENDING
NOT_EQ:
		ADD BX,4
CHECK_LOOP:
		LOOP START_LOOP
		
		MOV AX,[BP+4]
ENDING:
		POP BP
		RET
	_find_value ENDP
		END