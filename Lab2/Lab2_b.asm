	ARR3 DD 8 DUP(?)
	.CODE

start:
	MOV AX,@DATA
	MOV DS,AX
	MOV AX,ARR1 
	MOV WORD PTR ARR3,AX
	ADD AX,ARR1[2]
	MOV WORD PTR ARR3[4],AX
	ADD AX,ARR1[4]
	MOV WORD PTR ARR3[8],AX
	ADD AX,ARR1[6]
	MOV WORD PTR ARR3[12],AX
	.386
	MOV AL,ARR2
	XOR AH,AH
	MOV ARR3[16],EAX
	MOV CL,ARR2[1]
	MUL ECX
	MOV ARR3[20],EAX
	MOV CL,ARR2[2]
	MUL ECX
	MOV ARR3[24],EAX
	MOV CL,ARR2[3]
	MUL ECX
	MOV ARR3[28],EAX
	MOV AH,4CH
